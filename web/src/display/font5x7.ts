/** Hollow box for characters outside the table — same fallback as font.rs. */
const FALLBACK: readonly number[] = [0x7f, 0x41, 0x41, 0x41, 0x7f];

/** The 5 column bitmaps for `ch`, or the hollow-box fallback. */
export function glyph5x7(ch: string): readonly number[] {
  return TABLE[ch] ?? FALLBACK;
}

/** True when `ch` has a real glyph (used by the font-fidelity check). */
export function hasGlyph(ch: string): boolean {
  return ch in TABLE;
}
