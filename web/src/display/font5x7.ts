// Full 5×7 ASCII font, in the bit layout the firmware uses.
//
// Each glyph is 5 columns; each column is a 7-bit bitmap with **bit0 = top row**
// — identical to `calcumaker-matrix-fw/src/font.rs`. Where that file defines a
// glyph (space, 0-9, A-F, `-`, `.`), this table matches it byte-for-byte;
// `web/scripts/verify-font.mjs` parses font.rs and asserts it.
//
// Why a full table: font.rs is explicitly a SCAFFOLD ("TODO: a full 5×7 ASCII
// font") — it covers only digits/hex, uppercases its input, and renders anything
// else as a hollow box. The aux OLED shows status lines and messages such as
// "16C DEC RAD FLEX" and "register select cancelled", so it needs real letters,
// lowercase included. This is the table font.rs should adopt.

/** Columns per glyph (the 6th column of the 6px pitch is the inter-char gap). */
